package Model.MobilePhone;

public class PhoneDimensionsModel {
    private int width;
    private int height;
    private int weight;

    public void setWidth(int width){
        this.width=width;
    }
    public int getWidth(){
        return this.width;
    }
    public void setHeight(int height){
        this.height=height;
    }
    public int getHeight(){
        return this.height;
    }
    public void setWeight(int weight){
        this.weight=weight;
    }
    public int getWeight(){
        return this.weight;
    }
}
